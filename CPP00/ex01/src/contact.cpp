#include "contact.hpp"
#include <iostream>

void	Contact::set_info(
	const std::string& first,
	const std::string& last,
	const std::string& nick,
	const std::string& number,
	const std::string& secret)
{
	fst_name_ = first;
	lst_name_ = last;
	nick_name_ = nick;
	phone_nbr_ = number;
	dark_scrt_ = secret;
}

void Contact::display_info()
{
	std::cout << "First name: " << "'" << fst_name_ <<  "'" << std::endl;
	std::cout << "Last name: " << "'" << lst_name_ << "'" << std::endl;
	std::cout << "Nickname: " << "'" << nick_name_ << "'" << std::endl;
	std::cout << "Phonenumber: " << "'" << phone_nbr_ << "'" << std::endl;
	std::cout << "Darkest secret: " << "'" << dark_scrt_ << "'" << std::endl;
}